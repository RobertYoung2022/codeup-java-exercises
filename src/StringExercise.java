public class StringExercise {
    public static void main(String[] args) {
        String noEducation = "We don't need no education\nWe don't need no thought control";
        String insideS = "Check \"this\" out!, \"s inside of \"s!";
        String mainDrive = "In windows, the main drive is usually C:\\\\";
        String backslash = "I can do backslashes \\, double backslashes \\\\,\n" + "and the amazing triple backslash \\\\\\!";

        System.out.println(noEducation);
        System.out.println(insideS);
        System.out.println(mainDrive);
        System.out.println(backslash);
    }
}
